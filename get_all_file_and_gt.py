for name in name_list:
	print("Processing "+ name)
	seq = 0
	allow_set = set()
	deny_set = set()
	for url in url_list[name]:
		try:
			file_name = name + str(seq)
			jpg_name = path + "original/" + file_name + ".jpg"
			bmp_name = path + "300x300/" + file_name + ".bmp"
			urllib.request.urlretrieve(url, jpg_name)

			if os.path.getsize(jpg_name) < 40000:
				continue

			cmd = ['convert', jpg_name, '-resize', '300x300!', bmp_name] # convert image
			subprocess.check_call(cmd)

			attr, pred, acc = get_top_1_attribute_and_name_and_accuracy(bmp_name, model, model_name)


			if acc < 0.4 or attr in deny_set:
				continue

			if attr not in allow_set:
				print(name, attr, pred, acc)
				answer = input("OK to push to continue [Y/N]? ").lower()
				if answer != "y" and answer != "":
					deny_set.add(attr)
					continue
				else:
					allow_set.add(attr)

			attr_dict[file_name] = [attr, pred, acc]
			seq += 1
			if (seq == 10):
				print("----- Finish 10 images for " + name + "-----")
				allow_dict[name] = allow_set
				deny_dict[name] = deny_dict
				pickle.dump(attr, open("allow_dict.pkl", "wb"))
				pickle.dump(attr, open("deny_dict.pkl", "wb"))
				break
		except Exception as e:
			print(e)

